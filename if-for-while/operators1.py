# 运算符
# 算术运算符， +，  -，  *，  /，  //，  %，  **，  注意//为整除
# 赋值运算符， =， +=， -=， *=， /=， //= ，%=， **=
# 比较运算符， ==， !=， <>， >， >=， <， <=
# 逻辑运算符， and， or， not
# 成员运算符， in， not in
# 身份运算符， is， not is
# 位运算符，   &， |， >>， <<， ^， ~

# 优先级  算术>比较>逻辑>赋值

a = 3
b = 5
print(b // a)  # 1
a *= 3  # 9
print(a > b)  # True,9>5
print(3>4 and 4>2)  # False


